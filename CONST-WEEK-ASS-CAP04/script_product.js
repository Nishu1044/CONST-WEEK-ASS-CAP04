// =========== //
filter.addEventListener("change", function () {
    if (filter.value === "") {
        Display(productsData);
    } else {
        let filtered = productsData.filter(function (element) {
            if (element.Brand === filter.value) {
                return true;
            } else {
                return false;
            }
        })
        Display(filtered);
    }
})
