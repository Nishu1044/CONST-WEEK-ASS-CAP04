Display(productsData);
function Display(Data) {
    container.innerHTML = "";
    for (let i = 0; i < Data.length; i++) {
        const product = document.createElement("div");
        //---------------------------------------------------//
        const name = document.createElement("h3");
        name.innerText = Data[i].name

        const brand = document.createElement("h2");
        brand.innerText = Data[i].Brand

        const image = document.createElement("img");
        image.setAttribute("src", Data[i].selection3)

        const carName = document.createElement("h2");
        carName.innerText = Data[i].selection4

        const price = document.createElement("p");
        price.innerText = Data[i].selection5



        // =============///
        const addToCart = document.createElement("button");
        addToCart.innerText = "Cart";
        addToCart.style.backgroundColor = "red";
     
        addToCart.addEventListener("click", function () {
            alert("Added to the card")
            window.location.href="./cart.html"
            
            LSData.push(Data[i]);
            localStorage.setItem("cart", JSON.stringify(LSData));
        })

        container.append(product);
        product.append(name, brand, image, carName, price,addToCart)

    }
}











































